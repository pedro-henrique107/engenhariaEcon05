nome = "Pedro Machado"
disciplina = "Engenharia Econômica para Software"

# Função para exibir a mensagem
def exibir_mensagem(nome, disciplina):
    """
    Esta função recebe um nome e uma disciplina como parâmetros
    e exibe uma mensagem formatada.
    """
    print(f"Olá, meu nome é {nome}.")
    print(f"Estou estudando a disciplina de {disciplina}.")

# Chamando a função para exibir a mensagem
exibir_mensagem(nome, disciplina)
